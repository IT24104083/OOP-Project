
package com.example.demo;

public class customer {

    private String name;
    private String email;
    private String gender;

    public customer(String name, String email, String gender) {
        this.name = name;
        this.email = email;
        this.gender = gender;
    }



    public void display() {
        System.out.println(name + " " + email + " " + gender);

    }


    










}





